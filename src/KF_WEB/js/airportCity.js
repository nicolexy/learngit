AirportCity.getCityName = function(city){
    var tmp = "";
    if(AirportCity[city]){
        tmp = AirportCity[city].name;
    }
    return tmp;
}

//通过城市名获取机场三字码
AirportCity.getNameCity = function(name){
    var tmp = "";
    for(var key in AirportCity){
    	if(AirportCity[key].name == name){
    		tmp = key;
    	}
    }
    return tmp;
}
AirportCity.getInfo=function(arg){
	var info;
	if(typeof(arg.name)!='undefined'){
		for(var key in AirportCity){
			if(AirportCity[key].name == arg.name){
				info=AirportCity[key];
				info.code=key; 
				break;
			}
		}
	}else if(typeof(arg.code)!="undefined"){
		if(AirportCity[arg.code]){
			info=AirportCity[arg.code];
		}
	}
	return info;
}
